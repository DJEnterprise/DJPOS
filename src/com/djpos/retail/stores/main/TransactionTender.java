package com.djpos.retail.stores.main;

public class TransactionTender {

	private String transactionSubTotal;
	private String transactionDiscount;

	private String transactionTenderType;

	private String transactionTotal;

	public String getTransactionSubTotal() {
		return transactionSubTotal;
	}

	public void setTransactionSubTotal(String transactionSubTotal) {
		this.transactionSubTotal = transactionSubTotal;
	}

	public String getTransactionDiscount() {
		return transactionDiscount;
	}

	public void setTransactionDiscount(String transactionDiscount) {
		this.transactionDiscount = transactionDiscount;
	}

	public String getTransactionTenderType() {
		return transactionTenderType;
	}

	public void setTransactionTenderType(String transactionTenderType) {
		this.transactionTenderType = transactionTenderType;
	}

	public String getTransactionTotal() {
		return transactionTotal;
	}

	public void setTransactionTotal(String transactionTotal) {
		this.transactionTotal = transactionTotal;
	}
	
	

	
}
